export type UiKey = keyof typeof UI.en;

export function t(
  locale: Locale,
  key: UiKey,
  vars?: Record<string, string | number>
): string {
  let text: string = UI[locale][key] ?? UI.en[key];
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{{${k}}}`, String(v));
    }
  }
  return text;
}

const DIFFICULTY_KEYS = {
  easy: "difficultyEasy",
  moderate: "difficultyModerate",
  challenging: "difficultyChallenging",
  expert: "difficultyExpert",
} as const satisfies Record<TourDifficulty, UiKey>;

export function difficultyLabel(locale: Locale, difficulty: TourDifficulty): string {
  return t(locale, DIFFICULTY_KEYS[difficulty]);
}

